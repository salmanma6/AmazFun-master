public class follwgu {
    int pro;
    String name;
    String des;

    public follwgu(int pro,String name,String des) {
        this.pro = pro;
        this.name=name;
        this.des=des;
    }

    public int getPro() {
        return pro;
    }

    public void setPro(int pro) {
        this.pro = pro;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }
}
